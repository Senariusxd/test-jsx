{/* Operaciones de Calculos */}
export function sum(n1, n2) {
  return n1 + n2;
}

export function div(n1, n2) {
  return n1 / n2;
}

export function multi(n1, n2) {
  return n1 * n2;
}

export function rest(n1, n2) {
  return n1 - n2;
}
